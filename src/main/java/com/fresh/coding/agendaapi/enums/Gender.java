package com.fresh.coding.agendaapi.enums;

/**
 * Represents the gender of an individual.
 * This enum is used to specify the gender in various entities and applications.
 */
public enum Gender {

    /**
     * Male gender.
     */
    MALE,

    /**
     * Female gender.
     */
    FEMALE,

    /**
     * Other gender, for identities that do not fall into the male or female categories.
     */
    OTHER
}
